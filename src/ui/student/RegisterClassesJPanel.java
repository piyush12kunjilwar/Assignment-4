    private void addClassJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addClassJButtonActionPerformed
           int i = tblCourses.getSelectedRow();
           if(i<0){
               JOptionPane.showMessageDialog(this,"Please Select Class");
           return;
           }
           SeatAssignment sa = s.getTranscript().getCourseLoadBySemester(selectedSemester).newSeatAssignment(mastercoursecatalog.get(selectedSemester).getSchedule().get(i));
           refreshTable();
           if(sa==null){
           JOptionPane.showMessageDialog(this,"Class Full");
               return;
           }
           JOptionPane.showMessageDialog(this,"Class Added");
    }//GEN-LAST:event_addClassJButtonActionPerformed


