    private void populateComboBox(){
        semesterComboBox.removeAllItems();
        for(String semester: mastercoursecatalog.keySet()){
            semesterComboBox.addItem(semester);
        }
        semesterComboBox.setSelectedItem(selectedSemester);
    }
        public void refreshTable() {
        
        DefaultTableModel model = (DefaultTableModel)tblCourses.getModel();
        model.setRowCount(0);
        
        for(CourseOffer co : mastercoursecatalog.get(selectedSemester).getSchedule()) {
            Object row[] = new Object[4];
            row[0] = co.getSubjectCourse().getName();
            row[1] = co.getCourseNumber();
           
            row[3] = co.getRoomNumber();
            if(co.getFacultyProfile()==null){
                row[2] = "NA";
            }
            else{
                 row[2] = co.getFacultyProfile().getPerson().getPersonId();
            }
            model.addRow(row);
        }
        model = (DefaultTableModel) tblMyClasses.getModel();
        model.setRowCount(0);
        if(s.getTranscript().getCourseLoadBySemester(selectedSemester)==null){
            s.getTranscript().newCourseLoad(selectedSemester);
        }
        for(SeatAssignment sa : s.getTranscript().getCourseLoadBySemester(selectedSemester).getSeatAssignments()) {
            Object row[] = new Object[3];
            CourseOffer co = sa.getCourseOffer();
            
             row[0] = co.getSubjectCourse().getName();
            
           
            row[2] = co.getRoomNumber();
            if(co.getFacultyProfile()==null){
                row[1] = "NA";
            }
            else{
                 row[1] = co.getFacultyProfile().getPerson().getPersonId();
            }
            
            model.addRow(row);
            
            
        }
        
    }

