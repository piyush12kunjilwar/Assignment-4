    private void populateComboBoxes() {
       for(Course c : courseCatalog.getCourseList()){
           courseComboBox.addItem(c.getNumber());
       }
       facultyComboBox.addItem("No Teacher Assigned");
       for(FacultyProfile f : department.getFacultyDirectory().getTeacherlist()){
           facultyComboBox.addItem(f.getPerson().getPersonId());
       }
    }
    
    private void backAction() {
        workArea.remove(this);
        Component[] componentArray = workArea.getComponents();
        Component component = componentArray[componentArray.length - 1];
        //workArea.remove(this);
        CourseScheduleJPanel cs = (CourseScheduleJPanel) component;
        cs.refreshTable();
        CardLayout layout = (CardLayout) workArea.getLayout();
        layout.previous(workArea);
    }
    
