    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
         CourseOffer c = currentSchedule.newCourseOffer(courseComboBox.getSelectedItem().toString());
        if(!txtRoom.getText().equals("")){
            c.setRoomNumber(txtRoom.getText());
        }
        if(facultyComboBox.getSelectedIndex()!= 0){
            c.assignAsTeacher(department.getFacultyDirectory().findTeachingFaculty(facultyComboBox.getSelectedItem().toString()));
        }
      
        JOptionPane.showMessageDialog(this, "Course offer successfully added");
        backAction();
        
    }//GEN-LAST:event_saveButtonActionPerformed

